function Utility.BlockCreep(team,npcBot,lane)
	local flip = math.floor(DotaTime()*10)%2+1;
	local randomLR = {
		[1] = Vector(-150, 150),
		[2] = Vector(150, -150)
	};
	npcBot:Action_MoveToLocation(GetLocationAlongLane( team, GetLaneFrontAmount( team, lane, true )+0.04 )+randomLR[flip] );
end

