function Utility.GetDistance(a,b)
	return math.sqrt((a[1]-b[1])*(a[1]-b[1]) + (a[2]-b[2])*(a[2]-b[2]));
end

function Utility.GetPresence()
	local enemy;
	local presenceScore = 0;
	local IDs=GetTeamPlayers(TEAM_RADIANT+TEAM_DIRE-GetTeam());
	for i,id in pairs(IDs) do
		location,time = GetHeroLastSeenInfo( id );
		if (time ~= nil and time < 10) then
			presenceScore = presenceScore + 1;
		end
	end
	return presenceScore;
end

function Utility.GetLaneDistance(defendTeam,attackTeam,lane)
	local dist = 5000;
	local tower = nil;
	for towerNum = (lane-1)*3,(lane-1)*3+2 do
		tower = GetTower( defendTeam, towerNum );
		if tower ~= nil and tower:IsAlive() then
			dist = GetUnitToLocationDistance(tower, GetLaneFrontLocation( attackTeam, lane, 0 ));
			return dist,tower;
		end
	end
	return dist,tower;
end

function Utility.HeroNearTarget(enemy,target,radius)
	local numHero = 0;
	local oneDistance;
	local heroes = target:GetNearbyHeroes( radius, enemy, BOT_MODE_NONE );
	if heroes ~= nil then
		for _,v in pairs(heroes) do
			if v~=nil and v:CanBeSeen() then numHero = numHero + 1 end
		end
	end
	return numHero;
end

function Utility.CanFindHero(enemyHero)
	return (enemyHero:CanBeSeen() == true or enemyHero:GetTimeSinceLastSeen( ) < 10);
end

function Utility.DayNight()
	local time = DotaTime();
	local DayNight = math.floor(math.floor(time/60)/4)%2;
	local nextDayNight = 240;
	for hfcycle = 0,100 do
		nextDayNight = hfcycle*4*60 - time;
		if nextDayNight >= 0 and nextDayNight < 240 then
			break;
		end
	end
	return DayNight,nextDayNight;
end

function Utility.GetRune(npcBot,rune)
	local runeLoc = GetRuneSpawnLocation( rune );
	
	if GetUnitToLocationDistance(npcBot,runeLoc) > 100 then
		npcBot:Action_MoveToLocation( runeLoc );
		return false;
	elseif GetRuneStatus(rune) == RUNE_STATUS_AVAILABLE then
		npcBot:Action_PickUpRune( rune );
		return true;
	else
		return false;
	end
end

